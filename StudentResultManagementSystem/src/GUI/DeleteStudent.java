    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int option=JOptionPane.showConfirmDialog(null, 
                "Are you sure you wnat to delete student and student results?", "Delete", 
                JOptionPane.YES_NO_OPTION);
        if(option==JOptionPane.YES_OPTION){
            String rollno=RollNoTextField.getText();
            try {
                Connection con=ConnectionProviderCode.getcon();
                Statement st=con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
                String query1="DELETE FROM RESULT WHERE ROLLNO ="+rollno;
                st.execute(query1);
                String query2="DELETE FROM STUDENT WHERE ROLLNO ="+rollno;
                st.execute(query2);
                JOptionPane.showMessageDialog(null, "Student and student results Removed");
                this.setVisible(false);
                DeleteResult frame=new DeleteResult();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

